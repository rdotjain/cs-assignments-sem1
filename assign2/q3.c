#include <stdio.h>
int main()
{
    // \t is a horizontal tab character. 
    // It will provide a tab space between two words. 
    printf("Rupanshi \t Jain\n");

    // \n, is a new line character. 
    printf("Rupanshi \n Jain\n");

    // \b - backspace character transfers the cursor one character back with  
    // or without deleting on different compilers.
    printf("Rupanshi \b\b\b\b\b\b Jain\n");
}