#include <stdio.h>
#include <math.h>
int main ()
{
    printf("Demonstrating math functions\n");
    printf("sin(90 rad) ---> %.2f\n", sin(90));
    printf("cos(90 rad) ---> %.2f\n", cos(90));
    printf("log(100) base 10 ---> %.2f\n", log10(100));
    printf("2 raised to power 4 ---> %.0f\n", pow(2, 4));
    printf("square-root of 16 ---> %.2f\n", sqrt(16));
}